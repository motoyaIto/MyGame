Title::Title()
	: Scene()
{
	

	//デバック用////////////////////////////////////////////////////////////////
	//テキストの初期化
	Scene::m_text = new Text();
	Scene::m_text->SetPos(DirectX::SimpleMath::Vector2(0, 0));
	////////////////////////////////////////////////////////////////////////////
}

Title::~Title()
{

}

void Title::Initialize()
{
}

void Title::Update()
{
	

	//デバック用////////////////////////////////////////////////////////////////
	//シーン切替
	if (m_keyborad->GetKeyDown(DirectX::Keyboard::Space))
	{
		m_state = false;
	}
	////////////////////////////////////////////////////////////////////////////
}

void Title::Render()
{
	//デバック用////////////////////////////////////////////////////////////////
	//シーン名を表示
	Scene::m_text->Render(L"TitleScene");
	////////////////////////////////////////////////////////////////////////////
}
